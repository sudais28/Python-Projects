# 2. Outlier Detection
print("\n2. Outlier Detection for Precipitation:")

Q1 = data['Precipitation(in)'].quantile(0.25)
Q3 = data['Precipitation(in)'].quantile(0.75)
IQR = Q3 - Q1
lower_bound = Q1 - 1.5*IQR
upper_bound = Q3 + 1.5*IQR

outliers = data[(data['Precipitation(in)'] < lower_bound) |
                (data['Precipitation(in)'] > upper_bound)]

plt.figure(figsize=(10, 6))
sns.boxplot(x=data['Precipitation(in)'], color='skyblue')
plt.title('Precipitation Outliers Detection')
plt.xlabel('Precipitation (inches)')
plt.show()

print(f"""Outlier Analysis:
- Lower bound: {lower_bound:.2f} inches
- Upper bound: {upper_bound:.2f} inches
- {len(outliers)} outliers detected ({len(outliers)/len(data)*100:.1f}% of data)

Handling Outliers:
1. Consider retaining outliers as they represent rare but valid weather events
2. Employ robust statistics like median for more accurate analysis
3. Use log transformation to normalize skewed distributions
""")

