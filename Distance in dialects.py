import numpy as np
from scipy.cluster import hierarchy
import matplotlib.pyplot as plt

n = len(dialects)
distance_matrix = np.zeros((n, n))

for i in range(n):
    for j in range(i+1, n):
        sim = pair_sim[(dialects[i], dialects[j])]
        dist = 1 - sim
        distance_matrix[i][j] = dist
        distance_matrix[j][i] = dist

# condensed form for linkage
condensed = distance_matrix[np.triu_indices(n, k=1)]

Z = hierarchy.linkage(condensed, method='ward')

plt.figure(figsize=(14, 7))
hierarchy.dendrogram(Z, labels=dialects, leaf_rotation=90)
plt.title("Dialect Tree - How similar Pashto dialects are")
plt.xlabel("Cities/Dialects")
plt.ylabel("Distance (lower = more similar)")
