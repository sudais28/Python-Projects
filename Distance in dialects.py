# First, the edit distance function


def edit_distance(word1, word2):

    if len(word1) < len(word2):
        word1, word2 = word2, word1

    if len(word2) == 0:
        return len(word1)

    # initial row
    prev = list(range(len(word2) + 1))

    for i, char1 in enumerate(word1):
        current = [i + 1]
        for j, char2 in enumerate(word2):
            insert = prev[j + 1] + 1
            delete = current[j] + 1
            sub = prev[j] + (0 if char1 == char2 else 1)
            current.append(min(insert, delete, sub))
        prev = current

    return prev[-1]

