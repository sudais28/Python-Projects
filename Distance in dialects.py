# Now for similarity between two word entries
# Some have multiple words like "waṛīz, čorī" or "myērə stərgā"
# So I split them and take the pair with smallest distance (highest similarity)

def get_similarity(entry1, entry2):
    # split by comma, slash, or spaces
    parts1 = [p.strip() for p in entry1.replace(',', ' ').replace('/', ' ').split() if p.strip()]
    parts2 = [p.strip() for p in entry2.replace(',', ' ').replace('/', ' ').split() if p.strip()]

    if not parts1 or not parts2:
        return 0.0

    best = 0.0
    for p1 in parts1:
        for p2 in parts2:
            dist = edit_distance(p1, p2)
            # I used max length instead of sum because it gives better similarity for different lengths
            max_len = max(len(p1), len(p2))
            if max_len == 0:
                sim = 1.0
            else:
                sim = 1 - (dist / max_len)
            if sim > best:
                best = sim
    return best

